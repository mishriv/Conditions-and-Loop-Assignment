/* 
Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order.
 */

// numbers from 1 to 25 in ascending order
console.log("Ascending Order:");
let ascendingNum = 1;
while (ascendingNum <= 25) {
    console.log(ascendingNum);
    ascendingNum++;
}


// numbers from 25 to 1 in descending order
console.log("Descending Order:");
let descendingNum = 25;
while (descendingNum >= 1) {
    console.log(descendingNum);
    descendingNum--;
}
